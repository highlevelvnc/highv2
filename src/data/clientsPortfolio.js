// Funções auxiliares
export const getClientsByCountry = (country) => {
  if (country === 'Todos') return clientsData;
  return clientsData.filter(client => client.country === country);
};

export const getClientsByCategory = (category) => {
  return clientsData.filter(client => client.category === category);
};

export const searchClients = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return clientsData.filter(client => 
    client.name.toLowerCase().includes(term) ||
    client.category.toLowerCase().includes(term) ||
    client.country.toLowerCase().includes(term)
  );
};

export const getCountryCounts = () => {
  const counts = {};
  clientsData.forEach(client => {
    counts[client.country] = (counts[client.country] || 0) + 1;
  });
  return counts;
};

export const getCategoryCounts = () => {
  const counts = {};
  clientsData.forEach(client => {
    counts[client.category] = (counts[client.category] || 0) + 1;
  });
  return counts;
};

export const getTotalClients = () => clientsData.length;
export const getTotalCountries = () => [...new Set(clientsData.map(c => c.country))].length;
export const getTotalProjects = () => Math.round(clientsData.length * 1.2);
